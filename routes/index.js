/*******************************************************************************
  ~ router
*******************************************************************************/

const express = require ('express')

/***************************************
  setup router
***************************************/

const router = express.Router ()
const hello = {
  message : `hello! i'm ~ (the server).`,
}

/***************************************
  setup children
***************************************/

router.use ('/api', require ('./api'))

/***************************************
  handle requests
***************************************/

router.route ('/')
  .get ((ri, ro) => {
    ro
      .status (501)
      .json (hello)
  })

/**************************************/

/// exports ///
module.exports = router
